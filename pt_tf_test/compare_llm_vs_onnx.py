class LLMMethod:
    """
    Test case conversion method based on LLM (simplified version, 1 iteration)          Process: Extracting use cases from MongoDB → Executed in PT and TF → LLMJudgment repair/jump over → Execute use cases generated by LLM     Reused llm_enhanced_compare.py core logic, but streamlined output。
    """

