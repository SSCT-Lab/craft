    def convert_llm_test_cases(self, pt_tc: Dict, tf_tc: Dict) -> Tuple[Dict, Dict]:
        """Convert test cases returned by LLM into executable format (shared tensor data）"""
        shared_tensors = {}
        all_keys = set(pt_tc.keys()) | set(tf_tc.keys())
        for key in all_keys:
            if key == "api":
                continue
            pt_val = pt_tc.get(key)
            tf_val = tf_tc.get(key)
            tensor_desc = None
            if isinstance(pt_val, dict) and "shape" in pt_val:
                tensor_desc = pt_val
            elif isinstance(tf_val, dict) and "shape" in tf_val:
                tensor_desc = tf_val
            if tensor_desc:
                shared_tensors[key] = self.generate_numpy_data(tensor_desc)

        converted_pt = {}
        for k, v in pt_tc.items():
            converted_pt[k] = shared_tensors.get(k, v)
        converted_tf = {}
        for k, v in tf_tc.items():
            converted_tf[k] = shared_tensors.get(k, v)
        return converted_pt, converted_tf

