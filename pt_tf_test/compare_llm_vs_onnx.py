class OnnxConverter:
    """
    Implemented via ONNX intermediate format PyTorch → TensorFlow Test case migration          core process：
    1. Wrap PyTorch operators into a minimal nn.Module
    2. use torch.onnx.export Export as ONNX model
    3. Use onnxruntime inference to get the output          Success Criteria: ONNX Export + OnnxRuntime Successful reasoning is regarded as"ONNXMethod conversion successful"
    """

    def __init__(self, print_lock: Lock = None):
        self.print_lock = print_lock or Lock()
        self.execution_lock = RLock()

    def _safe_print(self, msg: str, end: str = "\n"):
        safe_print(msg, self.print_lock, end)

    def _wrap_as_module(self, torch_api: str, is_class_api: bool,
                        init_kwargs: Dict[str, Any],
                        extra_kwargs: Dict[str, Any] = None) -> Optional[torch.nn.Module]:
        """
        Wrap PyTorch operators as nn.Module，so that torch.onnx.export use
        
        Args:
            torch_api: PyTorch API name
            is_class_api: Is it in class form? API
            init_kwargs: Initialization parameters of class operators
            extra_kwargs: Non-tensor keyword arguments to function operators (captured via closures）
        
        Returns:
            packaged nn.Module，Return on failure None
        """
        parts = torch_api.split(".")
        extra_kwargs = extra_kwargs or {}

        try:
            if is_class_api:
                cls = self._resolve_attr(parts)
                if cls is None:
                    return None
                instance = cls(**init_kwargs)
                if isinstance(instance, torch.nn.Module):
                    return instance
                return _FuncWrapper(lambda x, _inst=instance: _inst(x))
            else:
                func = self._resolve_attr(parts)
                if func is None:
                    return None
                # If there are additional parameters that are not tensors, capture them through a closure
                if extra_kwargs:
                    return _FuncWrapper(lambda *args, _f=func, _kw=extra_kwargs: _f(*args, **_kw))
                return _FuncWrapper(func)
        except Exception:
            return None

    @staticmethod
    def _resolve_attr(parts: List[str]):
        """according to ['torch', 'nn', 'ReLU'] Such a path to obtain attributes"""
        try:
            obj = torch
            for p in parts[1:]:  # jump over 'torch'
                obj = getattr(obj, p)
            return obj
        except AttributeError:
            return None

    def convert_and_run(self, torch_api: str, test_case: Dict[str, Any],
                        is_class_api: bool) -> Dict[str, Any]:
        """
        Perform ONNX transformation on individual test cases + reasoning                  process：
        1. Prepare PyTorch input tensors
        2. Use PyTorch to forward propagate to obtain reference results
        3. torch.onnx.export Export
        4. onnxruntime reasoning
        5. Compare the results of the two
        
        Returns:
            {
                "onnx_export_success": bool,
                "onnx_run_success": bool,
                "pt_success": bool,
                "error": str or None,
                "pt_shape": list or None,
                "onnx_shape": list or None,
            }
        """
        result = {
            "onnx_export_success": False,
            "onnx_run_success": False,
            "pt_success": False,
            "error": None,
            "pt_shape": None,
            "onnx_shape": None,
        }

        # Use execution lock guarantee torch.onnx.export Thread safety
        with self.execution_lock:
            # ---------- 1. ready to enter ----------
            try:
                input_tensors, init_kwargs, input_names, extra_kwargs = self._prepare_inputs(test_case, is_class_api)
            except Exception as e:
                result["error"] = f"Input preparation failed: {e}"
                return result

            if not input_tensors:
                result["error"] = "No valid input tensor"
                return result

            # ---------- 2. packaging model ----------
            module = self._wrap_as_module(torch_api, is_class_api, init_kwargs, extra_kwargs)
            if module is None:
                result["error"] = f"Unable to wrap operator {torch_api} for nn.Module"
                return result

            module.eval()

            # ---------- 3. PyTorch forward propagation ----------
            try:
                with torch.no_grad():
                    if len(input_tensors) == 1:
                        pt_output = module(input_tensors[0])
                    else:
                        pt_output = module(*input_tensors)
                result["pt_success"] = True
                if hasattr(pt_output, 'shape'):
                    result["pt_shape"] = list(pt_output.shape)
            except Exception as e:
                result["error"] = f"PyTorchForward propagation failed: {e}"
                return result

            # ---------- 4. ONNX Export ----------
            onnx_path = None
            try:
                onnx_fd, onnx_path = tempfile.mkstemp(suffix=".onnx")
                os.close(onnx_fd)

                if len(input_tensors) == 1:
                    dummy_input = input_tensors[0]
                else:
                    dummy_input = tuple(input_tensors)

                output_names = ["output"]
                torch.onnx.export(
                    module,
                    dummy_input,
                    onnx_path,
                    input_names=input_names,
                    output_names=output_names,
                    opset_version=14,
                    do_constant_folding=True,
                )
                result["onnx_export_success"] = True
            except Exception as e:
                result["error"] = f"ONNXExport failed: {e}"
                if onnx_path and os.path.exists(onnx_path):
                    os.remove(onnx_path)
                return result

            # ---------- 5. OnnxRuntime reasoning ----------
            try:
                sess = ort.InferenceSession(onnx_path, providers=["CPUExecutionProvider"])
                feed = {}
                for i, tensor in enumerate(input_tensors):
                    name = input_names[i] if i < len(input_names) else f"input_{i}"
                    feed[name] = tensor.detach().cpu().numpy()

                onnx_outputs = sess.run(None, feed)
                onnx_result = onnx_outputs[0]

                result["onnx_run_success"] = True
                result["onnx_shape"] = list(onnx_result.shape)

            except Exception as e:
                result["error"] = f"OnnxRuntimeReasoning failed: {e}"
            finally:
                if onnx_path and os.path.exists(onnx_path):
                    os.remove(onnx_path)

        return result

    def _prepare_inputs(self, test_case: Dict[str, Any],
                        is_class_api: bool) -> Tuple[List[torch.Tensor], Dict[str, Any], List[str], Dict[str, Any]]:
        """
        Extract input tensors and initialization parameters from test case
        
        Returns:
            (input_tensorslist, init_kwargsdictionary, input_nameslist, extra_kwargsdictionary)
            extra_kwargs: The non-tensor keyword parameters of the function operator are passed to the wrapped through closure.Module
        """
        input_tensors: List[torch.Tensor] = []
        init_kwargs: Dict[str, Any] = {}
        input_names: List[str] = []
        extra_kwargs: Dict[str, Any] = {}  # Non-tensor parameters of function operators

        # List of positional parameter names (parameters that need to be converted into tensors)）
        positional_tensor_params = ["condition", "input", "x", "y", "other"]

        # Variable parameter handling
        varargs_key = None
        for key in test_case.keys():
            if key.startswith('*'):
                varargs_key = key
                break

        if varargs_key:
            varargs_value = test_case[varargs_key]
            if isinstance(varargs_value, list):
                for idx, item in enumerate(varargs_value):
                    tensor = self._to_torch_tensor(item)
                    if tensor is not None:
                        input_tensors.append(tensor)
                        input_names.append(f"input_{idx}")
            return input_tensors, init_kwargs, input_names, extra_kwargs

        # Collect positional parameters in order as input tensor
        for param_name in positional_tensor_params:
            if param_name in test_case:
                value = test_case[param_name]
                tensor = self._to_torch_tensor(value)
                if tensor is not None:
                    input_tensors.append(tensor)
                    input_names.append(param_name)

        # Other parameters
        skip_params = {"layout", "requires_grad", "out", "api"}
        for key, value in test_case.items():
            if key in positional_tensor_params or key in skip_params or key.startswith('*'):
                continue
            if is_class_api:
                # Class operator: non-tensor parameters as initialization parameters
                if isinstance(value, np.ndarray):
                    input_tensors.append(torch.from_numpy(value.copy()))
                    input_names.append(key)
                else:
                    init_kwargs[key] = value
            else:
                # function operator
                if isinstance(value, np.ndarray):
                    input_tensors.append(torch.from_numpy(value.copy()))
                    input_names.append(key)
                elif isinstance(value, (int, float, bool, str)):
                    # Non-tensor arguments are passed via closures
                    extra_kwargs[key] = value

        return input_tensors, init_kwargs, input_names, extra_kwargs

    @staticmethod
    def _to_torch_tensor(value: Any) -> Optional[torch.Tensor]:
        """Convert values ​​in various formats to PyTorch tensors"""
        if isinstance(value, np.ndarray):
            return torch.from_numpy(value.copy())
        elif isinstance(value, dict) and "shape" in value:
            dtype_map = {
                "torch.float64": np.float64, "torch.float32": np.float32,
                "torch.int64": np.int64, "torch.int32": np.int32,
                "torch.bool": np.bool_, "torch.uint8": np.uint8,
                "float64": np.float64, "float32": np.float32,
                "int64": np.int64, "int32": np.int32,
                "bool": np.bool_, "uint8": np.uint8,
            }
            shape = value.get("shape", [])
            dtype_str = value.get("dtype", "torch.float32")
            dtype = dtype_map.get(dtype_str, np.float32)
            if shape:
                if dtype == np.bool_:
                    arr = np.random.randint(0, 2, shape).astype(np.bool_)
                elif dtype in [np.int64, np.int32]:
                    arr = np.random.randint(-10, 10, shape).astype(dtype)
                else:
                    arr = np.random.randn(*shape).astype(dtype)
            else:
                arr = np.array(1.0, dtype=dtype)
            return torch.from_numpy(arr)
        elif isinstance(value, (int, float)):
            return torch.tensor(value)
        elif isinstance(value, list):
            try:
                return torch.tensor(value)
            except Exception:
                return None
        return None


class _FuncWrapper(torch.nn.Module):
    """Wrap a normal function as nn.Module，used for torch.onnx.export"""

    def __init__(self, func):
        super().__init__()
        self._func = func

    def forward(self, *args):
        return self._func(*args)


