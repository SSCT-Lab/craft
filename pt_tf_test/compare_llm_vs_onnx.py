# ==================== main function ====================

def main():
    parser = argparse.ArgumentParser(
        description="LLMmethod vs ONNX method：PyTorch→TensorFlowTest case conversion success rate comparison"
    )
    parser.add_argument("--num-cases", "-n", type=int, default=DEFAULT_NUM_CASES,
                        help=f"The number of test cases for each operator test (default{DEFAULT_NUM_CASES}）")
    parser.add_argument("--max-iterations", "-m", type=int, default=DEFAULT_MAX_ITERATIONS,
                        help=f"LLMThe number of iterations per use case of the method (default{DEFAULT_MAX_ITERATIONS}）")
    parser.add_argument("--start", type=int, default=1,
                        help="Starting operator index (starting from 1）")
    parser.add_argument("--end", type=int, default=None,
                        help="End operator index (including）")
    parser.add_argument("--operators", "-o", nargs="*",
                        help="Specify operator name list")
    parser.add_argument("--workers", "-w", type=int, default=DEFAULT_WORKERS,
                        help=f"LLMNumber of concurrent threads (default{DEFAULT_WORKERS}）")
    parser.add_argument("--model", default=DEFAULT_MODEL,
                        help=f"LLMmodel (default{DEFAULT_MODEL}）")
    parser.add_argument("--key-path", "-k", default=DEFAULT_KEY_PATH,
                        help=f"API keypath (default{DEFAULT_KEY_PATH}）")
    args = parser.parse_args()

    print("=" * 80)
    print("LLMmethod vs ONNX method — PyTorch→TensorFlow Test case conversion success rate comparison")
    print("=" * 80)
    print(f"📌 Number of use cases for each operator: {args.num_cases}")
    print(f"📌 LLMNumber of iterations: {args.max_iterations}")
    print(f"📌 Number of concurrent threads: {args.workers}")
    print(f"📌 LLMModel: {args.model}")
    print("=" * 80)

    comparator = LLMvsONNXComparator(
        key_path=args.key_path, model=args.model,
        num_workers=args.workers
    )

    start_time = time.time()

    try:
        # Get operator list
        all_docs = list(comparator.llm_method.collection.find({}, {"api": 1}))
        all_ops = [doc["api"] for doc in all_docs if "api" in doc]
        print(f"\n📋 Database CCP {len(all_ops)} an operator")

        if args.operators:
            operator_names = args.operators
        else:
            start_idx = max(1, args.start) - 1
            end_idx = args.end if args.end is not None else len(all_ops)
            end_idx = min(end_idx, len(all_ops))
            operator_names = all_ops[start_idx:end_idx]
            print(f"📌 Test range: No. {start_idx + 1} ~ {end_idx} an operator")

        print(f"📋 will test {len(operator_names)} an operator")
        print(f"📋 Top 10: {', '.join(operator_names[:10])}{'...' if len(operator_names) > 10 else ''}\n")

        # Run comparison
        result = comparator.run_comparison(
            operator_names,
            num_cases=args.num_cases,
            max_iterations=args.max_iterations
        )

        # output and save
        comparator.print_and_save_results(result)

        elapsed = time.time() - start_time
        h, m, s = int(elapsed // 3600), int((elapsed % 3600) // 60), int(elapsed % 60)
        print(f"\n⏱️ Total time spent: {h}h {m}m {s}s")

    finally:
        comparator.close()
        print("✅ Program execution completed")


if __name__ == "__main__":
    main()
