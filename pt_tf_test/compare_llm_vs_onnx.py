class LLMvsONNXComparator:
    """
    Main comparison category: Comparative test of unified management LLM method and ONNX method          Run two methods at the same time for each use case of each operator, and finally summarize the statistics。
    """

    def __init__(self, key_path: str = DEFAULT_KEY_PATH,
                 model: str = DEFAULT_MODEL,
                 num_workers: int = DEFAULT_WORKERS):
        self.print_lock = Lock()
        self.num_workers = max(1, num_workers)
        self.execution_lock = RLock()
        self.realtime_lock = Lock()

        self.llm_method = LLMMethod(
            key_path=key_path, model=model,
            print_lock=self.print_lock,
            llm_workers=self.num_workers
        )
        self.onnx_converter = OnnxConverter(print_lock=self.print_lock)

        # Results directory
        self.result_dir = os.path.join(ROOT_DIR, "pt_tf_test")
        os.makedirs(self.result_dir, exist_ok=True)
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        self.realtime_file_path = os.path.join(self.result_dir, f"llm_vs_onnx_realtime_{timestamp}.jsonl")
        self.realtime_file = open(self.realtime_file_path, "a", encoding="utf-8")

    def _safe_print(self, msg: str, end: str = "\n"):
        safe_print(msg, self.print_lock, end)

    def _append_realtime_record(self, record: Dict[str, Any]):
        """Write a single statistical record in real time（JSONL）"""
        with self.realtime_lock:
            self.realtime_file.write(json.dumps(record, ensure_ascii=False) + "\n")
            self.realtime_file.flush()

    def run_comparison(self, operator_names: List[str],
                       num_cases: int = DEFAULT_NUM_CASES,
                       max_iterations: int = DEFAULT_MAX_ITERATIONS) -> Dict[str, Any]:
        """
        Run LLM vs ONNX comparison test on a batch of operators                  Returns a dictionary of summary results
        """
        # global statistics
        global_stats = {
            "total_operators": len(operator_names),
            "tested_operators": 0,
            "skipped_operators_llm": 0,      # LLMSelect the number of operators to skip
            "skipped_operators_no_tf": 0,     # Number of operators without TF mapping
            "skipped_operators_deprecated": 0,  # The number of operators skipped by the version

            # LLM Method statistics (after excluding skipped operators)）
            "llm_total_cases": 0,            # LLMTotal number of TF test cases generated
            "llm_tf_success": 0,             # TFThe number of successful executions

            # ONNX Method statistics (after excluding skipped operators)）
            "onnx_total_cases": 0,           # ONNXTotal number of conversion attempts
            "onnx_export_success": 0,        # ONNXNumber of successful exports
            "onnx_run_success": 0,           # ONNXThe number of successful inferences (i.e."Conversion successful"）
        }

        operator_details = []  # Detailed results for each operator

        for idx, op_name in enumerate(operator_names, 1):
            self._safe_print(f"\n{'='*70}")
            self._safe_print(f"[{idx}/{len(operator_names)}] operator: {op_name}")
            self._safe_print(f"{'='*70}")

            # 1. Check API mapping
            torch_api, tf_api, mapping_method = self.llm_method.convert_api_name(op_name)
            if tf_api is None:
                self._safe_print(f"  ⏭️ No TF corresponding implementation（{mapping_method}），jump over")
                global_stats["skipped_operators_no_tf"] += 1
                operator_details.append({
                    "operator": op_name, "status": "skipped_no_tf",
                    "mapping_method": mapping_method
                })
                continue

            self._safe_print(f"  PT: {torch_api} → TF: {tf_api}")

            # 2. Get documents from MongoDB
            document = self.llm_method.collection.find_one({"api": op_name})
            if document is None:
                self._safe_print(f"  ❌ Not found in database")
                operator_details.append({"operator": op_name, "status": "not_found"})
                continue

            total_cases = self.llm_method.get_num_test_cases(document)
            actual_cases = min(num_cases, total_cases)

            # 3. Test both LLM and ONNX
            op_result = self._test_operator(
                op_name, torch_api, tf_api, document,
                actual_cases, max_iterations
            )

            operator_details.append(op_result)

            # 4. Summary statistics (operator that skips LLM skip）
            if op_result["status"] == "skipped_by_llm":
                global_stats["skipped_operators_llm"] += 1
            elif op_result["status"] == "skipped_deprecated":
                global_stats["skipped_operators_deprecated"] += 1
            else:
                global_stats["tested_operators"] += 1
                global_stats["llm_total_cases"] += op_result["llm_total"]
                global_stats["llm_tf_success"] += op_result["llm_tf_success"]
                global_stats["onnx_total_cases"] += op_result["onnx_total"]
                global_stats["onnx_export_success"] += op_result["onnx_export_success"]
                global_stats["onnx_run_success"] += op_result["onnx_run_success"]

        return {"global_stats": global_stats, "operator_details": operator_details}

    def _test_operator(self, op_name: str, torch_api: str, tf_api: str,
                       document: Dict, num_cases: int,
                       max_iterations: int) -> Dict[str, Any]:
        """
        Simultaneous testing of LLM and ONNX methods for multiple use cases of a single operator                  Using a thread pool for concurrent LLM calls, ONNX transformations and operator execution are executed sequentially through locks
        """
        op_result = {
            "operator": op_name,
            "torch_api": torch_api,
            "tf_api": tf_api,
            "status": "completed",
            "num_cases": num_cases,
            "llm_total": 0,
            "llm_tf_success": 0,
            "onnx_total": 0,
            "onnx_export_success": 0,
            "onnx_run_success": 0,
            "case_details": []
        }

        is_class_api = self.llm_method.is_class_based_api(torch_api)

        # Prepare all test cases
        cases = []
        for case_idx in range(num_cases):
            tc = self.llm_method.prepare_shared_numpy_data(document, case_index=case_idx)
            tc["api"] = torch_api
            cases.append((case_idx + 1, tc))

        # Pre-crawl documents (crawl only once）
        self._safe_print(f"  📖 Crawl API documentation...")
        torch_doc, tf_doc = self.llm_method.fetch_api_docs(torch_api, tf_api)

        # Use a thread pool to process each use case concurrently
        if self.num_workers <= 1:
            case_results = []
            for case_number, tc in cases:
                cr = self._process_single_case(
                    torch_api, tf_api, tc, case_number,
                    is_class_api, torch_doc, tf_doc, max_iterations
                )
                case_results.append(cr)
        else:
            case_results = []
            with ThreadPoolExecutor(max_workers=self.num_workers) as executor:
                future_map = {}
                for case_number, tc in cases:
                    future = executor.submit(
                        self._process_single_case,
                        torch_api, tf_api, tc, case_number,
                        is_class_api, torch_doc, tf_doc, max_iterations
                    )
                    future_map[future] = case_number
                for future in as_completed(future_map):
                    case_results.append(future.result())

        case_results.sort(key=lambda x: x["case_number"])

        # Check if skipped due to version obsolescence (priority higher thanLLM skip）
        any_deprecated = any(cr.get("deprecated_skip", False) for cr in case_results)
        if any_deprecated:
            op_result["status"] = "skipped_deprecated"
            op_result["case_details"] = case_results
            self._safe_print("  ⏭️ This operator has been eliminated from the version and will be skipped.")
            return op_result

        # Check if all use cases are covered LLM skip
        all_skipped = all(cr.get("llm_skipped", False) for cr in case_results)
        if all_skipped and len(case_results) > 0:
            op_result["status"] = "skipped_by_llm"
            op_result["case_details"] = case_results
            self._safe_print(f"  ⏭️ LLMChoose to skip this operator")
            return op_result

        # Aggregate results for each use case
        for cr in case_results:
            if not cr.get("llm_skipped", False):
                op_result["llm_total"] += cr.get("llm_total", 0)
                op_result["llm_tf_success"] += cr.get("llm_tf_success", 0)
                # ONNX statistics only count non-skipped cases to keep a consistent denominator
                op_result["onnx_total"] += cr.get("onnx_total", 0)
                op_result["onnx_export_success"] += cr.get("onnx_export_success", 0)
                op_result["onnx_run_success"] += cr.get("onnx_run_success", 0)
        op_result["case_details"] = case_results

        return op_result

    def _process_single_case(self, torch_api: str, tf_api: str,
                             test_case: Dict[str, Any], case_number: int,
                             is_class_api: bool,
                             torch_doc: str, tf_doc: str,
                             max_iterations: int) -> Dict[str, Any]:
        """
        Addressing a single use case: LLM approach and ONNX approach simultaneously                  LLM method flow (1 iteration）：
          1. Executed in PT and TF with DB use cases
          2. Call LLM to get fix/Mutations/jump over
          3. If LLM returnsmutation/repair，Execute use cases generated by LLM                  ONNX method flow：
          1. Use the same DB use case and wrap it intoPyTorch Module
          2. torch.onnx.export + onnxruntimereasoning
        """
        case_result = {
            "case_number": case_number,
            "llm_skipped": False,
            "deprecated_skip": False,
            "llm_total": 0,
            "llm_tf_success": 0,
            "onnx_total": 0,
            "onnx_export_success": 0,
            "onnx_run_success": 0,
            "llm_detail": None,
            "onnx_detail": None,
        }

        # ========== ONNX method ==========
        self._safe_print(f"  [use case{case_number}] ONNXmethod...", end="")
        with self.execution_lock:
            onnx_result = self.onnx_converter.convert_and_run(
                torch_api, test_case, is_class_api
            )
        case_result["onnx_total"] = 1
        if onnx_result["onnx_export_success"]:
            case_result["onnx_export_success"] = 1
        if onnx_result["onnx_run_success"]:
            case_result["onnx_run_success"] = 1

        onnx_status = "✓" if onnx_result["onnx_run_success"] else "✗"
        onnx_err = f" ({onnx_result['error'][:60]})" if onnx_result.get("error") else ""
        self._safe_print(f" ONNX:{onnx_status}{onnx_err}")
        case_result["onnx_detail"] = onnx_result

        # ========== LLM method ==========
        # Step 1: Execute in both frameworks with DB original use case
        torch_test_case = test_case
        tf_test_case = copy.deepcopy(test_case)
        tf_test_case["api"] = tf_api

        self._safe_print(f"  [use case{case_number}] LLMmethod: initial execution...", end="")
        try:
            with self.execution_lock:
                exec_result = self.llm_method._execute_sequential(
                    torch_api, tf_api, torch_test_case, tf_test_case
                )
            pt_s = "✓" if exec_result['torch_success'] else "✗"
            tf_s = "✓" if exec_result['tensorflow_success'] else "✗"
            self._safe_print(f" PT:{pt_s} TF:{tf_s}")
        except Exception as e:
            self._safe_print(f" ❌ Execution failed: {str(e)[:60]}")
            exec_result = {
                "torch_api": torch_api, "tensorflow_api": tf_api,
                "torch_success": False, "tensorflow_success": False,
                "results_match": False, "status": "fatal_error",
                "torch_error": str(e), "tensorflow_error": None,
                "comparison_error": None
            }

        # If PyTorch executes the error message containsdeprecated/removed，Skip this operator directly
        torch_error = str(exec_result.get("torch_error", ""))
        if not exec_result.get("torch_success", False) and torch_error:
            if re.search(r"deprecated|removed", torch_error, re.IGNORECASE):
                case_result["deprecated_skip"] = True
                case_result["llm_skipped"] = False
                case_result["llm_detail"] = {
                    "initial_exec": exec_result,
                    "llm_operation": "skip",
                    "llm_reason": "PyTorchThe operator has been eliminated by the version"
                }
                self._safe_print(" Version obsolescence detected, skipped")
                self._append_realtime_record({
                    "timestamp": datetime.now().isoformat(),
                    "operator": torch_api,
                    "case_number": case_number,
                    "status": "skipped_deprecated",
                    "onnx_run_success": bool(case_result.get("onnx_run_success", 0)),
                    "llm_tf_success": False,
                    "llm_skipped": False,
                    "deprecated_skip": True
                })
                return case_result

        # Step 2: CallLLM
        self._safe_print(f"  [use case{case_number}] callLLM...", end="")
        llm_result = self.llm_method.call_llm(
            exec_result, torch_test_case, tf_test_case, torch_doc, tf_doc
        )
        operation = llm_result.get("operation", "skip")
        reason = llm_result.get("reason", "")[:60]
        self._safe_print(f" {operation} - {reason}")

        if operation == "skip":
            case_result["llm_skipped"] = True
            case_result["llm_detail"] = {
                "initial_exec": exec_result,
                "llm_operation": operation,
                "llm_reason": llm_result.get("reason", "")
            }
            self._append_realtime_record({
                "timestamp": datetime.now().isoformat(),
                "operator": torch_api,
                "case_number": case_number,
                "status": "llm_skip",
                "onnx_run_success": bool(case_result.get("onnx_run_success", 0)),
                "llm_tf_success": False,
                "llm_skipped": True,
                "deprecated_skip": False
            })
            return case_result

        # Step 3: If LLM returnsmutation/repair，Execute use cases generated by LLM
        llm_pt_tc = llm_result.get("pytorch_test_case", torch_test_case)
        llm_tf_tc = llm_result.get("tensorflow_test_case", tf_test_case)

        # Convert to executable format
        try:
            llm_pt_tc, llm_tf_tc = self.llm_method.convert_llm_test_cases(llm_pt_tc, llm_tf_tc)
        except Exception as e:
            case_result["llm_skipped"] = True
            case_result["llm_detail"] = {
                "initial_exec": exec_result,
                "llm_operation": "skip",
                "llm_reason": f"LLMThe returned shape is illegal or the tensor cannot be generated.: {e}"
            }
            self._safe_print(f"  [use case{case_number}] LLMUse case conversion failed, skipped: {str(e)[:60]}")
            self._append_realtime_record({
                "timestamp": datetime.now().isoformat(),
                "operator": torch_api,
                "case_number": case_number,
                "status": "llm_case_convert_failed",
                "onnx_run_success": bool(case_result.get("onnx_run_success", 0)),
                "llm_tf_success": False,
                "llm_skipped": True,
                "deprecated_skip": False
            })
            return case_result

        case_result["llm_total"] = 1

        self._safe_print(f"  [use case{case_number}] Execute LLM use cases...", end="")
        try:
            with self.execution_lock:
                llm_exec = self.llm_method._execute_sequential(
                    torch_api, tf_api, llm_pt_tc, llm_tf_tc
                )
            pt_s = "✓" if llm_exec['torch_success'] else "✗"
            tf_s = "✓" if llm_exec['tensorflow_success'] else "✗"
            match_s = "✓" if llm_exec['results_match'] else "✗"
            self._safe_print(f" PT:{pt_s} TF:{tf_s} Match:{match_s}")

            if llm_exec["tensorflow_success"]:
                case_result["llm_tf_success"] = 1

        except Exception as e:
            self._safe_print(f" ❌ {str(e)[:60]}")
            llm_exec = {
                "torch_success": False, "tensorflow_success": False,
                "results_match": False, "status": "fatal_error",
                "error": str(e)
            }

        case_result["llm_detail"] = {
            "initial_exec": exec_result,
            "llm_operation": operation,
            "llm_reason": llm_result.get("reason", ""),
            "llm_exec": llm_exec
        }

        self._append_realtime_record({
            "timestamp": datetime.now().isoformat(),
            "operator": torch_api,
            "case_number": case_number,
            "status": "completed",
            "onnx_run_success": bool(case_result.get("onnx_run_success", 0)),
            "llm_tf_success": bool(case_result.get("llm_tf_success", 0)),
            "llm_skipped": False,
            "deprecated_skip": False
        })

        return case_result

